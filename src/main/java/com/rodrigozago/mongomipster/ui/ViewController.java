    private void jButtonInserirNaComandaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirNaComandaActionPerformed
        if(this.jTableServicoProdutos.getSelectedRow() > -1)
        {
            Comanda comanda = this.database.getComanda(this.jTextFieldServicoIdDaComanda.getText());
            Produto pro = this.database.getProduto(this.modelProdutos.getValueAt(this.jTableServicoProdutos.getSelectedRow(), 0).toString());
            comanda.addProduto(pro);
            this.database.updateOneDocument("comandas", "_id", comanda.getId(), comanda.getDocument());
            // atualizar jtable
            
            ArrayList<Produto> produtosDaComanda = comanda.getProdutos();
            this.modelServicoProdutosDaComanda.setRowCount(0);
            double total = 0.0;
            for (Iterator<Produto> i = produtosDaComanda.iterator(); i.hasNext();) {
                Produto aux = i.next();
                Object[] row = {aux.getNome(), "R$: " + aux.getPreco()};
                this.modelServicoProdutosDaComanda.addRow(row);
                total += Double.parseDouble(pro.getPreco());
            }
            total += Double.parseDouble(pro.getPreco());
            this.jTextFieldServicoViewTotalComanda.setText("R$ " + String.valueOf(total));
            JOptionPane.showMessageDialog(null, "Produto inserido com sucesso!", "Produto inserido", JOptionPane.INFORMATION_MESSAGE);
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione um produto antes de adicionar.", "Selecione um produto", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonInserirNaComandaActionPerformed

