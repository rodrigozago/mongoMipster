    private void jTextFieldCaixaIdComandaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCaixaIdComandaKeyReleased
        if(Verificador.verificaIdComanda(this.jTextFieldCaixaIdComanda.getText()))
        {
            Comanda comanda = this.database.getComanda(this.jTextFieldCaixaIdComanda.getText());
            if(comanda != null)
            {
                this.jTextFieldCaixaViewNomeCliente.setText(comanda.getCliente().getNome());
                this.jTextFieldCaixaViewClienteCPF.setText(comanda.getCliente().getCpf());
                ArrayList<Produto> produtosDaComanda = comanda.getProdutos();
                if(produtosDaComanda.isEmpty())
                    this.jTextFieldCaixaViewTotalComanda.setText("R$ 00,00");
                else
                {
                    double total = 0.0;
                    for(Iterator<Produto> i = produtosDaComanda.iterator(); i.hasNext();)
                    {
                        Produto pro = i.next();
                        Object[] row = {pro.getNome(), "R$: " + pro.getPreco()};
                        total += Double.parseDouble(pro.getPreco());
                        this.modelProdutosDaComanda.addRow(row);
                    }
                    this.totalDaComanda = total;
                    this.jTextFieldCaixaViewTotalComanda.setText("R$ " + String.valueOf(total));
                }
                this.jButtonCaixaFecharComanda.setEnabled(true);
            }
            else
            {
                this.jTextFieldCaixaCPFDoCliente.setEnabled(true);
                this.jButtonCaixaAbrirComanda.setEnabled(false);
                this.jTextFieldCaixaViewNomeCliente.setText("");
                this.jTextFieldCaixaViewClienteCPF.setText("");
                this.jTextFieldCaixaViewTotalComanda.setText("");
                this.modelProdutosDaComanda.setRowCount(0);
                this.totalDaComanda = 0.0;
                this.jButtonCaixaFecharComanda.setEnabled(false);
            }
        }
        else
        {
            this.jTextFieldCaixaCPFDoCliente.setEnabled(false);
            this.jButtonCaixaAbrirComanda.setEnabled(false);
            this.jTextFieldCaixaViewNomeCliente.setText("");
            this.jTextFieldCaixaViewClienteCPF.setText("");
            this.jTextFieldCaixaViewTotalComanda.setText("");
            this.modelProdutosDaComanda.setRowCount(0);
            this.totalDaComanda = 0.0;
            this.jButtonCaixaFecharComanda.setEnabled(false);
        }
            
    }//GEN-LAST:event_jTextFieldCaixaIdComandaKeyReleased

